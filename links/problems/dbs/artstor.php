<p>
    
    Artstor is currently working very slowly, and in some cases not at all, from off-campus. We are working with the vendor to remedy this problem. In the meantime, you can use the following process to access Artstor.
</p>
<p><strong>Do you have an Artstor user account?</strong></p>
<p>If you do, <a  class="db-name" href="http://library.artstor.org/">go to Artstor</a> and sign in.</p>
<p>If you do not:</p>

<ol>
    <li>Visit the landing page of Artstor via <a class="db-name" href="http://0-library.artstor.org.lasiii.losrios.edu/library/">this link.</a></li>
    <li>Register for a free Artstor account.</li>
    <li>Once you've registered for the account, visit Artstor directly at <a class="db-name" href="http://library.artstor.org/">http://library.artstor.org/</a> rather than going through the Los Rios Research Databases page. Sign in using the account you created.</li>
</ol>
